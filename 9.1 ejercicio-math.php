<?php
    $num1 = 5.6;
    $num2 = -2.8;

    $ceil_num1 = ceil($num1);
    $ceil_num2 = ceil($num2);

    echo "Ceil pentru $num1 este: " . $ceil_num1 . "<br>"; // Output: Ceil pentru 5.6 este: 6
    echo "Ceil pentru $num2 este: " . $ceil_num2 . "<br>"; // Output: Ceil pentru -2.8 este: -2

    $num1 = 5.6;
    $num2 = -2.8;

    $floor_num1 = floor($num1);
    $floor_num2 = floor($num2);

    echo "Floor pentru $num1 este: " . $floor_num1 . "<br>"; // Output: Floor pentru 5.6 este: 5
    echo "Floor pentru $num2 este: " . $floor_num2 . "<br>"; // Output: Floor pentru -2.8 este: -3
?>